import { app } from 'electron';
import Runtime, { winService } from '@modern-js/runtime/electron-main';

const runtime = new Runtime({
  windowsConfig: [
    {
      name: 'main',
    },
  ],
  mainServices: {
    test: () => 'hello, modern js electron',
  },
});

app.whenReady().then(async () => {
  await runtime.init();

  winService.createWindow({
    name: 'main',
  });
});
